package barisic.newsgetter.helper_classes;

public class Article {
    String title;
    String url;
    String urlToImage;
    String description;

    public String getTitle() {
        return title;
    }

    public String getArticleUrl() {
        return url;
    }

    public String getImageUrl() {
        return urlToImage;
    }

    public String getDescription() {
        return description;
    }
}
